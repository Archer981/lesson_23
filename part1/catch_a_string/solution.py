# В функции `cant_work` есть строка, выполнение которой
# може вызвать исключение и некорректно завершить программу
# Обработайте исключение и выведите текст Исключение поймано

a = [1, 2, 3]


def cant_work():
    try:
        value = a[4]
    except:
        value = "Исключение поймано"
    return value
